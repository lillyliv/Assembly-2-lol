    print('including std lib...')
    file.write(';;Functions from library std\n')
    file.write('dump:\n')
    file.write('    ;;dump\n')
    file.write('    pop r10\n')
    file.write('    pop rsi\n')
    file.write('    mov [toDump], rsi\n')
    file.write('    mov rsi, toDump\n')
    file.write('    mov rax, 1\n')
    file.write('    mov rdi, 1\n')
    file.write('    mov rdx, 8\n')
    file.write('    syscall\n')
    file.write('    push r10\n')
